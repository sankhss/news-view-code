import UIKit

class ThemeManager {
    
    static func backgroundColor() -> UIColor{
        return UIColor(displayP3Red: 47/255, green: 54/255, blue: 64/255, alpha: 1.0)
    }
    
    static func contrastColor() -> UIColor{
        return .white
    }
}
